// --- MVI: move immediate to register ---
// Format: "imm8:XX -> r:YY"

{
    const regNames = ["B", "C", "D", "E", "H", "L", "M", "A"];
    const opcodes = [0x06, 0x0e, 0x16, 0x1e, 0x26, 0x2e, 0x36, 0x3e];

    for (let r = 0; r < 8; r++) {
        const { cpu, memory } = createCPU();
        const results: string[] = [];

        if (r === 6) {
            cpu.set_rp(4, 0x4000); // HL points to 0x4000 for M
        }

        for (let v = 0; v < 256; v++) {
            cpu.pc = 0;
            memory.write_raw(0, v);
            cpu.execute(opcodes[r]);

            const result = r === 6 ? memory.read_raw(0x4000) : cpu.reg(r);
            results.push(`imm8:${hex8(v)} -> r:${hex8(result)}`);
        }

        const name = `mvi_${regNames[r].toLowerCase()}`;
        await writeData(name, name.toUpperCase(), results);
    }
}

// --- LDAX/STAX ---
