// --- DAD: 16-bit add to HL ---
// Format: "hl:XXXX rp:YYYY -> hl:ZZZZ cf:C"

type DadOp = { name: string; opcode: number; rpIndex: number };

const dadOps: DadOp[] = [
    { name: "dad_b", opcode: 0x09, rpIndex: 0 },
    { name: "dad_d", opcode: 0x19, rpIndex: 2 },
    { name: "dad_h", opcode: 0x29, rpIndex: 4 },
    { name: "dad_sp", opcode: 0x39, rpIndex: 6 },
];

for (const op of dadOps) {
    const { cpu } = createCPU();
    const results: string[] = [];

    // Test subset: 256 HL values × 256 RP values (stepping by 256)
    for (let hl = 0; hl < 0x10000; hl += 0x100) {
        for (let rp = 0; rp < 0x10000; rp += 0x100) {
            cpu.pc = 0;
            cpu.set_rp(4, hl);
            if (op.rpIndex === 4) {
                // DAD H: HL + HL, rp is same as HL
            } else if (op.rpIndex === 6) {
                cpu.sp = rp;
            } else {
                cpu.set_rp(op.rpIndex, rp);
            }
            cpu.cf = 0;
            cpu.execute(op.opcode);

            const rpVal = op.rpIndex === 4 ? hl : rp;
            results.push(`hl:${hex16(hl)} rp:${hex16(rpVal)} -> hl:${hex16(cpu.hl())} cf:${cpu.cf}`);
        }
    }

    const varName = op.name.toUpperCase();
    await writeData(op.name, varName, results);
}

// --- INX/DCX: 16-bit increment/decrement ---
// Format: "rp:XXXX -> rp:YYYY"

type InxOp = { name: string; opcode: number; rpIndex: number };

const inxOps: InxOp[] = [
    { name: "inx_b", opcode: 0x03, rpIndex: 0 },
    { name: "inx_d", opcode: 0x13, rpIndex: 2 },
    { name: "inx_h", opcode: 0x23, rpIndex: 4 },
    { name: "inx_sp", opcode: 0x33, rpIndex: 6 },
    { name: "dcx_b", opcode: 0x0b, rpIndex: 0 },
    { name: "dcx_d", opcode: 0x1b, rpIndex: 2 },
    { name: "dcx_h", opcode: 0x2b, rpIndex: 4 },
    { name: "dcx_sp", opcode: 0x3b, rpIndex: 6 },
];

for (const op of inxOps) {
    const { cpu } = createCPU();
    const results: string[] = [];

    // Test boundary values and sampling
    const testValues = [0x0000, 0x0001, 0x00ff, 0x0100, 0x7fff, 0x8000, 0xfffe, 0xffff];
    for (let v = 0; v < 0x10000; v += 0x100) testValues.push(v);
    const unique = [...new Set(testValues)].sort((a, b) => a - b);

    for (const v of unique) {
        cpu.pc = 0;
        if (op.rpIndex === 6) cpu.sp = v;
        else cpu.set_rp(op.rpIndex, v);
        cpu.execute(op.opcode);

        const result = op.rpIndex === 6 ? cpu.sp : cpu.rp(op.rpIndex);
        results.push(`rp:${hex16(v)} -> rp:${hex16(result)}`);
    }

    const varName = op.name.toUpperCase();
    await writeData(op.name, varName, results);
}

// --- PUSH/POP PSW round-trip ---
// Format: "a:XX flags:FFFFFFFF -> a:YY flags:GGGGGGGG"

{
    const { cpu, memory } = createCPU();
    const results: string[] = [];

    for (let a = 0; a < 256; a++) {
        for (let f = 0; f < 256; f += 4) { // sample flags
            cpu.pc = 0;
            cpu.sp = 0x2000;
            cpu.set_a(a);
            cpu.retrieve_flags(f);
            const origFlags = flags(cpu);

            cpu.execute(0xf5); // PUSH PSW

            // Corrupt A and flags
            cpu.set_a(0x00);
            cpu.retrieve_flags(0x00);

            cpu.execute(0xf1); // POP PSW

            results.push(`a:${hex8(a)} flags:${origFlags} -> a:${hex8(cpu.a())} flags:${flags(cpu)}`);
        }
    }

    await writeData("push_pop_psw", "PUSH_POP_PSW", results);
}

// --- MOV r,r: all 64 register-to-register moves (excluding M) ---
// Format: "mov R,R: src:XX -> dst:XX"

{
    const { cpu } = createCPU();
    const results: string[] = [];
    const regNames = ["B", "C", "D", "E", "H", "L", "M", "A"];

    for (let dst = 0; dst < 8; dst++) {
        if (dst === 6) continue; // skip M (memory)
        for (let src = 0; src < 8; src++) {
            if (src === 6) continue; // skip M
            for (let v = 0; v < 256; v += 17) { // sample values
                cpu.pc = 0;
                // Reset all regs
                for (let r = 0; r < 8; r++) if (r !== 6) cpu.set_reg(r, 0);
                cpu.set_reg(src, v);

                const opcode = 0x40 | (dst << 3) | src;
                cpu.execute(opcode);

                const dstVal = cpu.reg(dst);
                results.push(`mov ${regNames[dst]},${regNames[src]}: src:${hex8(v)} -> dst:${hex8(dstVal)}`);
            }
        }
    }

    await writeData("mov", "MOV", results);
}

