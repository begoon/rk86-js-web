// Generates test data tables for i8080 ALU and single-operand instructions.
// Run: bun tests/generate_cpu_data.ts

import { I8080 } from "../src/lib/i8080.ts";
import { Memory } from "../src/lib/rk86_memory.js";
import { hex8, hex16 } from "../src/lib/hex.js";

async function writeData(name: string, varName: string, results: string[]) {
    await Bun.write(
        `tests/cpu/${name}_data.ts`,
        `// auto-generated by generate_cpu_data.ts\nexport const ${varName}: string[] = [\n${results.map((r) => `    "${r}",`).join("\n")}\n];\n`,
    );
    console.log(`${name}: ${results.length} entries`);
}

const io = {
    input: (_port: number): number => 0,
    output: (_port: number, _value: number): void => {},
    interrupt: (_iff: number): void => {},
};

function createCPU(): { cpu: I8080; memory: Memory } {
    const memory = new Memory(undefined);
    const cpu = new I8080({ memory, io });
    return { cpu, memory };
}

function flags(cpu: I8080): string {
    return cpu.store_flags().toString(2).padStart(8, "0");
}

// --- DAA ---
// Format: "cf:C hf:H a:XX -> a:YY flags:FFFFFFFF"

{
    const { cpu } = createCPU();
    const results: string[] = [];

    for (let cf = 0; cf < 2; cf++) {
        for (let hf = 0; hf < 2; hf++) {
            for (let a = 0; a < 256; a++) {
                cpu.cf = cf;
                cpu.hf = hf;
                cpu.set_a(a);
                cpu.execute(0x27); // DAA
                results.push(`cf:${cf} hf:${hf} a:${hex8(a)} -> a:${hex8(cpu.a())} flags:${flags(cpu)}`);
            }
        }
    }

    await writeData("daa", "DAA", results);
}

// --- CPI ---
// Format: "a:XX imm8:YY -> a:ZZ flags:FFFFFFFF"

{
    const { cpu, memory } = createCPU();
    const results: string[] = [];

    for (let a = 0; a < 256; a++) {
        for (let imm8 = 0; imm8 < 256; imm8++) {
            cpu.pc = 0;
            cpu.set_a(a);
            cpu.retrieve_flags(0x00);
            memory.write_raw(0, imm8);
            cpu.execute(0xfe); // CPI
            results.push(`a:${hex8(a)} imm8:${hex8(imm8)} -> a:${hex8(cpu.a())} flags:${flags(cpu)}`);
        }
    }

    await writeData("cpi", "CPI", results);
}

// --- ALU immediate: ADI, ACI, SUI, SBI, ANI, ORI, XRI ---
// Format: "a:XX imm8:YY -> a:ZZ flags:FFFFFFFF"
// For ACI/SBI, also includes carry: "cf:C a:XX imm8:YY -> a:ZZ flags:FFFFFFFF"

type AluImm = { name: string; opcode: number; hasCarry: boolean };

const aluImmOps: AluImm[] = [
    { name: "adi", opcode: 0xc6, hasCarry: false },
    { name: "aci", opcode: 0xce, hasCarry: true },
    { name: "sui", opcode: 0xd6, hasCarry: false },
    { name: "sbi", opcode: 0xde, hasCarry: true },
    { name: "ani", opcode: 0xe6, hasCarry: false },
    { name: "ori", opcode: 0xf6, hasCarry: false },
    { name: "xri", opcode: 0xee, hasCarry: false },
];

for (const op of aluImmOps) {
    const { cpu, memory } = createCPU();
    const results: string[] = [];

    const carries = op.hasCarry ? [0, 1] : [0];
    for (const cf of carries) {
        for (let a = 0; a < 256; a++) {
            for (let imm8 = 0; imm8 < 256; imm8++) {
                cpu.pc = 0;
                cpu.set_a(a);
                cpu.retrieve_flags(0x00);
                cpu.cf = cf;
                memory.write_raw(0, imm8);
                cpu.execute(op.opcode);

                const prefix = op.hasCarry ? `cf:${cf} ` : "";
                results.push(`${prefix}a:${hex8(a)} imm8:${hex8(imm8)} -> a:${hex8(cpu.a())} flags:${flags(cpu)}`);
            }
        }
    }

    await writeData(op.name, op.name.toUpperCase(), results);
}

