{
    const { cpu } = createCPU();
    const results: string[] = [];

    for (const start of [0, 1]) {
        cpu.iff = start;
        cpu.pc = 0;
        cpu.execute(0xfb); // EI
        results.push(`ei: iff:${start} -> iff:${cpu.iff}`);
    }
    for (const start of [0, 1]) {
        cpu.iff = start;
        cpu.pc = 0;
        cpu.execute(0xf3); // DI
        results.push(`di: iff:${start} -> iff:${cpu.iff}`);
    }
    await writeData("ei_di", "EI_DI", results);
}

